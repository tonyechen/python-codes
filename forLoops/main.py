import time

# for loop =    a statement that will execute its block of code a limited amount of times



for i in range(10):
    print(i)

for i in range(50, 100, 2):
    print(i)

for i in "Tony Chen":
    print(i)

for i in range(10, 0, -1):
    print(i)
    time.sleep(1)
